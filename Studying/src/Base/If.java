package Base;

public class If {
    public static void main(String[] args) {
        if (10 < 11) System.out.println("10 < 11");
        if (10 <= 11) System.out.println("10 <= 11");
        if (10 > 9) System.out.println("10 > 9");
        if (10 >= 10) System.out.println("10 >= 10");
        if (10 == 10) System.out.println("10 == 10");
        if (10 != 9) System.out.println("10 != 9");
        if (10 != 10) System.out.println("You wont see this string");
    }
}
