package DataTypes_n_Operators;

public class Chars {
    public static void main(String[] args) {
        for (char a = 0; a <= 1000; a++){ /* тип char может быть выражен целочисленным значением и содержать символ,
                                             соответствующий этому значению по кодировке UTF - 8. Примечательно, что
                                             char может быть подвержен арифметическим операциям*/
            System.out.println(a);
        }

    }
}
