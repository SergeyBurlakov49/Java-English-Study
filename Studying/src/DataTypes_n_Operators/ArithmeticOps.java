package DataTypes_n_Operators;

public class ArithmeticOps {
    public static void main(String[] args) {
        System.out.println("4 * 2 = " + 4 * 2);
        System.out.println("4 + 2 = " + 4 + 2);
        System.out.println("4 - 2 = " + (4 - 2));
        System.out.println("4 / 2 = " + (4 / 2));
        System.out.println("Residue from dividing 4 by 2 = " + 4 % 2);
    }
}
